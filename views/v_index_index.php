    			<h3>Get Ready to Juice!</h3>
    				<p>Making fresh juice a part of a well-balanced, plant-based diet is an important tool for achieving good health.</p>

					<p>Juicing offers many life-enhancing health benefits including a faster, more efficient way to absorb immune boosting nutrients naturally found in fruits and vegetables. It provides a way to access digestive enzymes typically locked away in the fiber matrix of whole fruits and vegetables.</p>

					<p>Most commercial juices are processed and lacking in nutrition while freshly juiced fruits and vegetables are loaded with an abundance of vitamins, minerals and phytonutrients.</p>
					
					<p>Drinking fresh juice can help us adopt healthier eating patterns. For those of us who do not traditionally consume many fruits and vegetables, incorporating fresh juice can be a fun and different approach to increasing consumption of these important plant foods for improved health and wellness and reaching your weight loss goals.</p>
					
					<p>How does it work? Juicing removes the insoluble fiber from vegetables and fruits. While fiber is an established, important part of an overall healthy diet, removing the insoluble fiber allows for increased absorption of specific health promoting phytonutrients including enzymes, while the soluble fiber persists into the juice.  By removing the fibers and consuming fruits and vegetables in liquid form, we are providing a nutrient delivery system to our bodies that allows individuals who would otherwise have difficulty consuming whole vegetables, the opportunity to reap the numerous benefits vegetables have to offer.</p>
			
